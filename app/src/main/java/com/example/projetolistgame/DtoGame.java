package com.example.projetolistgame;

public class DtoGame {
    private int imagemMeme;
    private String descricao;
    private String anoCriacao;

    public int getImagemMeme() {
        return imagemMeme;
    }

    public void setImagemMeme(int imagemMeme) {
        this.imagemMeme = imagemMeme;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getAnoCriacao() {
        return anoCriacao;
    }

    public void setAnoCriacao(String anoCriacao) {
        this.anoCriacao = anoCriacao;
    }
}
